// Helper function to get resources by domain and type
export const getRealResources = (domain, type, limit = 24) => {
  const domainKey = findDomainKey(domain);
  const resources = REAL_RESOURCES[type]?.[domainKey] || [];
  
  // Add generic resources if we don't have enough specific ones
  if (resources.length < limit) {
    const genericResources = generateGenericResources(domain, type, limit - resources.length);
    return [...resources, ...genericResources].slice(0, limit);
  }
  
  return resources.slice(0, limit);
};

// Find matching domain key
const findDomainKey = (domain) => {
  const domainLower = domain?.toLowerCase() || '';
  
  // Direct matches
  const directMatches = ['javascript', 'python', 'react', 'nodejs', 'angular', 'vue'];
  for (const match of directMatches) {
    if (domainLower.includes(match)) {
      return match;
    }
  }
  
  // Partial matches
  if (domainLower.includes('js') || domainLower.includes('node')) {
    return 'javascript';
  }
  if (domainLower.includes('reactjs') || domainLower.includes('react.js')) {
    return 'react';
  }
  
  return 'javascript'; // Default fallback
};

// Generate generic resources for domains we don't have specific resources for
const generateGenericResources = (domain, type, limit) => {
  const resources = [];
  
  if (type === 'youtube') {
    for (let i = 0; i < limit; i++) {
      resources.push({
        title: `${domain} Tutorial ${i + 1}`,
        // eslint-disable-next-line no-script-url
        url: `https://www.youtube.com/results?search_query=${encodeURIComponent(domain + ' tutorial')}`,
        channel: "Search Results",
        thumbnail: "",
        description: `Learn ${domain} with this comprehensive tutorial`
      });
    }
  }
  // Add similar logic for other types
  
  return resources;
};